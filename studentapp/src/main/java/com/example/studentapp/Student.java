package com.example.studentapp;

import java.util.Date;

public class Student {
	private int stdId;
	private Date dob;
	/* getter and setters */
	public int getStdId() {
		return stdId;
	}
	public void setStdId(int stdId) {
		this.stdId = stdId;
	}
	public Date getDob() {
		return dob;
	}
	public void setDob(Date dob) {
		this.dob = dob;
	}
}
